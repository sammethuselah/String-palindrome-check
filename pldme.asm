DATA SEGMENT
	BLOCK1 DB 'TATTARRATTAT'
	MSG1 DB "IT'S A PALINDROME$"
	MSG2 DB "IT'S NOT A PALINDROME$"
DATA ENDS

PRINT MACRO MSG
	MOV AH, 09H
	LEA DX, MSG
	INT 21H
	INT 3H
ENDM

EXTRA SEGMENT
	BLOCK2 DB 12 DUP(?)
EXTRA ENDS

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA, ES:EXTRA
START: 
	MOV AX, DATA
	MOV DS, AX
	MOV AX, EXTRA
	MOV ES, AX
	LEA SI, BLOCK1
	LEA DI, BLOCK2+11
	MOV CX, 0CH
BACK: 
	CLD
	LODSB
	STD
	STOSB
	LOOP BACK
	LEA SI, BLOCK1
	LEA DI, BLOCK2
	MOV CX, 09H
	CLD
	REPZ CMPSB
	JNZ SKIP
	PRINT MSG1
SKIP: 
	PRINT MSG2
	CODE ENDS
END START